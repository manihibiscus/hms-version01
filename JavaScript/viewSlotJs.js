function submitFunction1(v) {
    let message = "Appointment slot is satisfied click ok button\nif it is not click cancel button.";
    if (confirm(message) == true) {
        message = "<p style='color:black;font-size:15px'>Conformed</p>";
    } else {
        message = "<p style='color:red;font-size:15px'>Cancelled!</p>";
    }
    document.getElementById("mess1").innerHTML = message;
    v.disabled=true;
}
